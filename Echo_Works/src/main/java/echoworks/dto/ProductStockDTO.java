package echoworks.dto;

public class ProductStockDTO {
    private int productStockNo;
    private int productStockPno;
    private String productStockOption;
    private int productStockStock;
    private int productStockPrice;

    public int getProductStockNo() {
        return productStockNo;
    }

    public void setProductStockNo(int productStockNo) {
        this.productStockNo = productStockNo;
    }

    public int getProductStockPno() {
        return productStockPno;
    }

    public void setProductStockPno(int productStockPno) {
        this.productStockPno = productStockPno;
    }

    public String getProductStockOption() {
        return productStockOption;
    }

    public void setProductStockOption(String productStockOption) {
        this.productStockOption = productStockOption;
    }

    public int getProductStockStock() {
        return productStockStock;
    }

    public void setProductStockStock(int productStockStock) {
        this.productStockStock = productStockStock;
    }

    public int getProductStockPrice() {
        return productStockPrice;
    }

    public void setProductStockPrice(int productStockPrice) {
        this.productStockPrice = productStockPrice;
    }
}
